package exercicios;

public class ProdutoEletrônico {
	
	private String nomeProduto;
	private String marcaProduto;
	private int anoLancamentoProduto;
	private double precoProduto;
	private String paisCriacaoProduto;
	public String getNomeProduto() {
		return nomeProduto;
	}
	public void setNomeProduto(String nomeProduto) {
		this.nomeProduto = nomeProduto;
	}
	public String getMarcaProduto() {
		return marcaProduto;
	}
	public void setMarcaProduto(String marcaProduto) {
		this.marcaProduto = marcaProduto;
	}
	public int getAnoLancamentoProduto() {
		return anoLancamentoProduto;
	}
	public void setAnoLancamentoProduto(int anoLancamentoProduto) {
		this.anoLancamentoProduto = anoLancamentoProduto;
	}
	public double getPrecoProduto() {
		return precoProduto;
	}
	public void setPrecoProduto(double precoProduto) {
		this.precoProduto = precoProduto;
	}
	public String getPaisCriacaoProduto() {
		return paisCriacaoProduto;
	}
	public void setPaisCriacaoProduto(String paisCriacaoProduto) {
		this.paisCriacaoProduto = paisCriacaoProduto;
	}
	
	
	
}
