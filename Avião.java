package exercicios;

public class Avião {
private String marcaAviao;
private String destinoViagem;
private int numeroPassageiros;
private double horarioSaida;
public String getMarcaAviao() {
	return marcaAviao;
}
public void setMarcaAviao(String marcaAviao) {
	this.marcaAviao = marcaAviao;
}
public String getDestinoViagem() {
	return destinoViagem;
}
public void setDestinoViagem(String destinoViagem) {
	this.destinoViagem = destinoViagem;
}
public int getNumeroPassageiros() {
	return numeroPassageiros;
}
public void setNumeroPassageiros(int numeroPassageiros) {
	this.numeroPassageiros = numeroPassageiros;
}
public double getHorarioSaida() {
	return horarioSaida;
}
public void setHorarioSaida(double horarioSaida) {
	this.horarioSaida = horarioSaida;
}


}
